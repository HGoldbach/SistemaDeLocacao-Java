    public Cliente getClienteFormulario() {
        String nome = tNome.getText();
        String sobrenome = tSobrenome.getText();
        String cpf = tCPF.getText();
        String rg = tRG.getText();
        String endereco = tRua.getText() + "," + tNumero.getText().trim() + "," + tCidade.getText() + "," + tUF.getText() + "," + tCEP.getText();
        return new Cliente(nome,sobrenome,rg,cpf,endereco);
    }
    
    public void limparCampos() {
        tNome.setText("");
        tSobrenome.setText("");
        tCPF.setText("");
        tRG.setText("");
        tRua.setText("");
        tNumero.setText("");
        tCidade.setText("");
        tUF.setText("");
        tCEP.setText("");
        tNome.setEditable(true);
        tSobrenome.setEditable(true);
        tRG.setEditable(true);
        tCPF.setEditable(true);
    }
    
    public void formataCampo() {
        try {
            MaskFormatter maskN = new MaskFormatter("####");
            MaskFormatter maskUF = new MaskFormatter("UU");
            MaskFormatter maskCEP = new MaskFormatter("#####-###");
            maskN.install(tNumero);
            maskUF.install(tUF);
            maskCEP.install(tCEP);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "ERRO! Algum campo foi mal preenchido!");
        }
    }
    
    public void setCliente(Cliente cliente) {
        this.clienteSelecionadoParaAtualizacao = cliente;
        tNome.setText(cliente.getNome());
        tSobrenome.setText(cliente.getSobrenome());
        tRG.setText(cliente.getRG());
        tCPF.setText(cliente.getCPF());
        String[] endereco = cliente.getEndereco().split(",");
        tRua.setText(endereco[0]);
        tNumero.setText(endereco[1]);
        tCidade.setText(endereco[2]);
        tUF.setText(endereco[3]);
        tCEP.setText(endereco[4]);
        tNome.setEditable(false);
        tSobrenome.setEditable(false);
        tRG.setEditable(false);
        tCPF.setEditable(false); 
    }
    
    public Cliente getClienteParaAtualizar() {
        if(clienteSelecionadoParaAtualizacao == null){
            return null;
        }
        clienteSelecionadoParaAtualizacao.setNome(tNome.getText());
        clienteSelecionadoParaAtualizacao.setSobrenome(tSobrenome.getText());
        clienteSelecionadoParaAtualizacao.setCPF(tCPF.getText());
        clienteSelecionadoParaAtualizacao.setRG(tRG.getText());
        String endereco = tRua.getText() + "," + tNumero.getText().trim() + "," + tCidade.getText() + "," + tUF.getText() + "," + tCEP.getText();
        clienteSelecionadoParaAtualizacao.setEndereco(endereco);
        return clienteSelecionadoParaAtualizacao;      
    }

    
