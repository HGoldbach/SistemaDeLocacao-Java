    private void formataCampo() {
        try {
            MaskFormatter maskA = new MaskFormatter("####");
            MaskFormatter maskV = new MaskFormatter("########");
            MaskFormatter maskP = new MaskFormatter("UUU-####");
            maskP.install(tPlaca);
            maskV.install(tValor);
            maskA.install(tAno);
        }
        catch (Exception err) {
            JOptionPane.showMessageDialog(this, "ERRO! Algum campo foi mal preenchido!");
        }
    }
    
    private void limparCampos() {
        marcaCombo.setModel(new DefaultComboBoxModel<>(new String[] {
            "--- Selecione Marca ---", "VW", "GM", "Fiat",  "Honda",  "Mercedes", "Yahama"
        }));
        modeloCombo.setModel(new DefaultComboBoxModel<>(new String[] {
            "--- Selecione Modelo ---"
        }));
        estadoCombo.setModel(new DefaultComboBoxModel<>(new String[] {
            "--- Selecione Estado ---", "DISPONIVEL"
        }));
        categoriaCombo.setModel(new DefaultComboBoxModel<>(new String[] {
            "--- Selecione Categoria ---"
        }));
        tValor.setText("");
        tPlaca.setText("");
        tAno.setText("");
    }
    
    

