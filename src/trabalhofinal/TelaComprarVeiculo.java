    private void bIncluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bIncluirMouseClicked
        Marca marca = null;
        Estado estado = null;
        Categoria categoria = null;
        double valor = 0;
        int ano = 0;
        String placa = "";
        String modelo = "";
        try {
            marca = Marca.valueOf(marcaCombo.getSelectedItem().toString());
            estado = Estado.valueOf(estadoCombo.getSelectedItem().toString());
            categoria = Categoria.valueOf(categoriaCombo.getSelectedItem().toString());
            valor = Double.parseDouble(tValor.getText());
            ano = Integer.parseInt(tAno.getText());
            placa = tPlaca.getText();
            modelo = modeloCombo.getSelectedItem().toString();
        }
        catch (Exception err) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos para realizar a compra!");
        }


        if (placa.equals("   -    ")) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos para realizar a compra!");
        } 
        else if (ano > 2022 || ano < 1960) {
            JOptionPane.showMessageDialog(this, "Insira um ano vÃ¡lido!");
        }
        else {
            switch(modelo) {
                case "Gol":
                case "Celta":
                case "Palio":
                case "Nivus":   
                case "Pulse": 
                case "Sedan":
                    try {
                        ModeloAutomovel enumA = ModeloAutomovel.valueOf(modelo);
                        this.adicionaVeiculo(new Automovel(marca, estado, null, categoria, valor, placa, ano, enumA));
                        JOptionPane.showMessageDialog(this, "Automovel inserido com sucesso");
                        limparCampos();
                    }
                    catch (Exception err) {
                        JOptionPane.showMessageDialog(this, "Preencha todos os campos para realizar a compra!");
                    }
                    break;
                case "FAZER":
                case "CROSSER":
                case "BIZ":
                case "TWISTER":
                    try {
                        ModeloMotocicleta enumM = ModeloMotocicleta.valueOf(modelo);
                        this.adicionaVeiculo(new Motocicleta(marca, estado, null, categoria, valor, placa, ano, enumM));
                        JOptionPane.showMessageDialog(this, "Motocicleta inserida com sucesso");
                        limparCampos();
                    }
                    catch (Exception err) {
                        JOptionPane.showMessageDialog(this, "Preencha todos os campos para realizar a compra!");
                    }
                    break;
                case "Kombi":
                case "Sprinter":
                    try {
                        ModeloVan enumV = ModeloVan.valueOf(modelo);
                        this.adicionaVeiculo(new Van(marca, estado, null, categoria, valor, placa, ano, enumV));
                        JOptionPane.showMessageDialog(this, "Van inserida com sucesso");
                        limparCampos();
                    }
                    catch (Exception err) {
                        JOptionPane.showMessageDialog(this, "Preencha todos os campos para realizar a compra!");
                    }
                    break;     
            }
        }
        
        
       
    }//GEN-LAST:event_bIncluirMouseClicked

